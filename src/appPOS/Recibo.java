package appPOS;


public class Recibo {
	
	private String resumenPuntos;
	private String resumenProductos;
	private String nombreCliente;
	
	
	
	//Metodos
	
	public String getResumenPuntos() {
		return resumenPuntos;
	}
	public void setResumenPuntos(String resumenPuntos) {
		this.resumenPuntos = resumenPuntos;
	}
	public String getResumenProductos() {
		return resumenProductos;
	}
	public void setResumenProductos(String resumenProductos) {
		this.resumenProductos = resumenProductos;
	}
	public String getNombreCliente() {
		return nombreCliente;
	}
	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}
	

}
